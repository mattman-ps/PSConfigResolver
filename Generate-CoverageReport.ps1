#!/usr/bin/env pwsh
<#
.SYNOPSIS
    Generate comprehensive Pester code coverage reports.

.DESCRIPTION
    Analyzes Pester code coverage results and generates:
    - Detailed text-based coverage summary
    - HTML interactive coverage report
    - Coverage metrics and statistics
    - Identification of untested code paths

.PARAMETER CoverageXmlPath
    Path to the coverage.xml file generated by Pester (CoverageGutters format).

.PARAMETER OutputPath
    Directory where reports will be generated. Default: ./results

.PARAMETER Format
    Report formats to generate. Options: Html, Text, All. Default: All

.PARAMETER Threshold
    Minimum coverage percentage threshold (0-100). Warnings will be generated for
    coverage below this percentage. Default: 80

.PARAMETER IncludeUntested
    If specified, includes detailed list of untested code paths in the report.

.EXAMPLE
    .\Generate-CoverageReport.ps1 -CoverageXmlPath ".\results\coverage.xml"
    Generates both HTML and text coverage reports.

.EXAMPLE
    .\Generate-CoverageReport.ps1 -CoverageXmlPath ".\results\coverage.xml" -Format Html -Threshold 85
    Generates HTML report only with 85% coverage threshold.

.EXAMPLE
    .\Generate-CoverageReport.ps1 -CoverageXmlPath ".\results\coverage.xml" -IncludeUntested
    Generates reports with detailed untested code paths.
#>
[CmdletBinding()]
param(
    [Parameter(Mandatory = $true)]
    [ValidateScript({ Test-Path -LiteralPath $_ })]
    [string]$CoverageXmlPath,

    [Parameter(Mandatory = $false)]
    [string]$OutputPath = './results',

    [Parameter(Mandatory = $false)]
    [ValidateSet('Html', 'Text', 'All')]
    [string]$Format = 'All',

    [Parameter(Mandatory = $false)]
    [ValidateRange(0, 100)]
    [int]$Threshold = 80,

    [Parameter(Mandatory = $false)]
    [switch]$IncludeUntested
)

# Ensure output directory exists
if (-not (Test-Path -LiteralPath $OutputPath)) {
    New-Item -ItemType Directory -Path $OutputPath -Force | Out-Null
}

<#
.SYNOPSIS
    Parse coverage XML and extract coverage data.
#>
function ConvertFrom-CoverageXml {
    param([string]$XmlPath)

    # Load XML with settings that prevent attempting to load external DTDs
    $xmlSettings = New-Object System.Xml.XmlReaderSettings
    $xmlSettings.DtdProcessing = [System.Xml.DtdProcessing]::Ignore
    $xmlSettings.IgnoreComments = $true
    $xmlSettings.IgnoreWhitespace = $true
    
    $xml = New-Object System.Xml.XmlDocument
    $xmlReader = [System.Xml.XmlReader]::Create($XmlPath, $xmlSettings)
    try {
        $xml.Load($xmlReader)
    }
    finally {
        $xmlReader.Dispose()
    }
    
    $coverage = @()

    # Parse JACOCO format coverage (generated by Pester)
    # JACOCO format: report > package > class > method > counter (with LINE type)
    foreach ($package in $xml.SelectNodes('//package')) {
        $packageName = $package.GetAttribute('name')
        
        foreach ($class in $package.SelectNodes('class')) {
            $fileName = $class.GetAttribute('sourcefilename')
            $className = $class.GetAttribute('name')
            
            # Get full path
            $fullPath = if ($packageName -and $fileName) {
                "$packageName/$fileName"
            } else {
                $className
            }
            
            $fileData = @{
                Path           = $fullPath
                LineCount      = 0
                CoveredLines   = 0
                UncoveredLines = @()
                CoverageDetail = @()
                ClassName      = $className
            }

            # Get LINE counter from class level
            $lineCounter = $class.SelectSingleNode("counter[@type='LINE']")
            if ($lineCounter) {
                $missed = [int]$lineCounter.GetAttribute('missed')
                $covered = [int]$lineCounter.GetAttribute('covered')
                
                $fileData.LineCount = $missed + $covered
                $fileData.CoveredLines = $covered
                
                # For JACOCO format, we don't have detailed line-by-line coverage at class level
                # So we'll mark all as covered if covered count > 0
                for ($i = 0; $i -lt $fileData.LineCount; $i++) {
                    if ($i -lt $fileData.CoveredLines) {
                        $fileData.CoverageDetail += @{
                            LineNumber = $i + 1
                            Covered    = $true
                        }
                    }
                    else {
                        $fileData.CoverageDetail += @{
                            LineNumber = $i + 1
                            Covered    = $false
                        }
                        $fileData.UncoveredLines += ($i + 1)
                    }
                }
            }

            # Always set coverage percentage
            if ($fileData.LineCount -gt 0) {
                $fileData.Coverage = [math]::Round(($fileData.CoveredLines / $fileData.LineCount) * 100, 2)
            }
            else {
                $fileData.Coverage = 0
            }

            $coverage += $fileData
        }
    }

    return $coverage
}

<#
.SYNOPSIS
    Generate coverage statistics.
#>
function Get-CoverageStatistics {
    param([array]$CoverageData)

    $stats = @{
        TotalFiles       = $CoverageData.Count
        TotalLines       = 0
        TotalCovered     = 0
        TotalUncovered   = 0
        FileStats        = @()
        HighestCoverage  = 0
        LowestCoverage   = 100
        AverageCoverage  = 0
        ByStatus         = @{}
    }

    foreach ($file in $CoverageData) {
        $stats.TotalLines += $file.LineCount
        $stats.TotalCovered += $file.CoveredLines
        $stats.TotalUncovered += ($file.LineCount - $file.CoveredLines)

        # Track file-level stats
        $stats.FileStats += @{
            Path           = $file.Path
            Coverage       = $file.Coverage
            CoveredLines   = $file.CoveredLines
            UncoveredLines = $file.UncoveredLines.Count
            TotalLines     = $file.LineCount
        }

        # Track min/max
        if ($file.Coverage -gt $stats.HighestCoverage) {
            $stats.HighestCoverage = $file.Coverage
        }
        if ($file.Coverage -lt $stats.LowestCoverage -and $file.LineCount -gt 0) {
            $stats.LowestCoverage = $file.Coverage
        }
    }

    # Calculate averages
    if ($stats.TotalLines -gt 0) {
        $stats.OverallCoverage = [math]::Round(($stats.TotalCovered / $stats.TotalLines) * 100, 2)
    }
    else {
        $stats.OverallCoverage = 0
    }

    # Categorize by coverage percentage
    $stats.ByStatus = @{
        Excellent = @($stats.FileStats | Where-Object { $_.Coverage -ge 90 }).Count
        Good      = @($stats.FileStats | Where-Object { $_.Coverage -ge 75 -and $_.Coverage -lt 90 }).Count
        Fair      = @($stats.FileStats | Where-Object { $_.Coverage -ge 50 -and $_.Coverage -lt 75 }).Count
        Poor      = @($stats.FileStats | Where-Object { $_.Coverage -lt 50 }).Count
    }

    return $stats
}

<#
.SYNOPSIS
    Generate text-based coverage report.
#>
function New-TextCoverageReport {
    param(
        [array]$CoverageData,
        [hashtable]$Statistics,
        [string]$OutputPath,
        [int]$Threshold,
        [bool]$IncludeUntested
    )

    $reportPath = Join-Path -Path $OutputPath -ChildPath 'coverage-report.txt'
    $report = @()

    $report += "=" * 80
    $report += "PESTER CODE COVERAGE REPORT"
    $report += "=" * 80
    $report += ""
    $report += "Generated: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
    $report += "Coverage Threshold: $Threshold%"
    $report += ""

    # Overall statistics
    $report += "OVERALL STATISTICS"
    $report += "-" * 80
    $report += "Overall Coverage:     $($Statistics.OverallCoverage)%"
    $report += "Total Files:          $($Statistics.TotalFiles)"
    $report += "Total Lines:          $($Statistics.TotalLines)"
    $report += "Covered Lines:        $($Statistics.TotalCovered)"
    $report += "Uncovered Lines:      $($Statistics.TotalUncovered)"
    $report += ""

    # Coverage by status
    $report += "COVERAGE DISTRIBUTION"
    $report += "-" * 80
    $report += "Excellent (90-100%):  $($Statistics.ByStatus.Excellent) files"
    $report += "Good (75-89%):        $($Statistics.ByStatus.Good) files"
    $report += "Fair (50-74%):        $($Statistics.ByStatus.Fair) files"
    $report += "Poor (<50%):          $($Statistics.ByStatus.Poor) files"
    $report += ""

    # Coverage threshold status
    $report += "THRESHOLD ANALYSIS"
    $report += "-" * 80
    if ($Statistics.OverallCoverage -ge $Threshold) {
        $report += "✓ Overall coverage MEETS threshold ($($Statistics.OverallCoverage)% >= $Threshold%)"
    }
    else {
        $report += "✗ Overall coverage BELOW threshold ($($Statistics.OverallCoverage)% < $Threshold%)"
        $report += "  Gap: $($Threshold - $Statistics.OverallCoverage)%"
    }
    $report += ""

    # Per-file breakdown
    $report += "FILE COVERAGE BREAKDOWN"
    $report += "-" * 80
    $report += "Sorted by coverage (highest to lowest):"
    $report += ""

    $sorted = $Statistics.FileStats | Sort-Object -Property Coverage -Descending

    foreach ($file in $sorted) {
        $status = switch ($file.Coverage) {
            { $_ -ge 90 } { "✓" }
            { $_ -ge 75 } { "→" }
            { $_ -ge 50 } { "!" }
            default { "✗" }
        }

        $filename = Split-Path -Leaf -Path $file.Path
        $report += "$status $filename"
        $report += "  Coverage: $($file.Coverage)% ($($file.CoveredLines)/$($file.TotalLines) lines)"

        if ($file.Coverage -lt $Threshold) {
            $report += "  WARNING: Below threshold by $($Threshold - $file.Coverage)%"
        }

        if ($IncludeUntested -and $file.UncoveredLines.Count -gt 0) {
            $maxShow = 10
            $uncovered = $file.UncoveredLines[0..([math]::Min($maxShow - 1, $file.UncoveredLines.Count - 1))]
            $report += "  Uncovered lines: $($uncovered -join ', ')"
            if ($file.UncoveredLines.Count -gt $maxShow) {
                $report += "  ... and $($file.UncoveredLines.Count - $maxShow) more"
            }
        }
        $report += ""
    }

    # Files below threshold
    $belowThreshold = $Statistics.FileStats | Where-Object { $_.Coverage -lt $Threshold } | Sort-Object -Property Coverage
    if ($belowThreshold.Count -gt 0) {
        $report += ""
        $report += "FILES BELOW THRESHOLD"
        $report += "-" * 80
        foreach ($file in $belowThreshold) {
            $filename = Split-Path -Leaf -Path $file.Path
            $report += "$filename - $($file.Coverage)% (Gap: $($Threshold - $file.Coverage)%)"
        }
    }

    $report += ""
    $report += "=" * 80

    # Write report
    $report | Out-File -LiteralPath $reportPath -Encoding utf8
    return $reportPath
}

