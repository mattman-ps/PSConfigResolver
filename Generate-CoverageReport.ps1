# ============================================================================
# Main Script Logic
# ============================================================================

try {
    Write-Host ""
    Write-Host "PSConfigResolver Code Coverage Report Generator" -ForegroundColor Cyan
    Write-Host "================================================" -ForegroundColor Cyan
    Write-Host ""

    # Parse coverage data
    Write-Host "  Parsing coverage data..." -NoNewline
    $coverageData = ConvertFrom-CoverageXml -XmlPath $CoverageXmlPath
    Write-Host " Done" -ForegroundColor Green

    # Generate statistics
    Write-Host "  Calculating statistics..." -NoNewline
    $stats = Get-CoverageStatistics -CoverageData $coverageData
    Write-Host " Done" -ForegroundColor Green

    # Generate reports
    if ($Format -in @('Text', 'All')) {
        Write-Host "  Generating text report..." -NoNewline
        $textReport = New-TextCoverageReport -CoverageData $coverageData -Statistics $stats `
            -OutputPath $OutputPath -Threshold $Threshold -IncludeUntested $IncludeUntested
        Write-Host " Done" -ForegroundColor Green
    }

    if ($Format -in @('Html', 'All')) {
        Write-Host "  Generating HTML report..." -NoNewline
        $htmlReport = New-HtmlCoverageReport -CoverageData $coverageData -Statistics $stats `
            -OutputPath $OutputPath -Threshold $Threshold
        Write-Host " Done" -ForegroundColor Green
    }

    Write-Host ""
    Write-Host "Coverage Summary:" -ForegroundColor Cyan
    Write-Host "  Overall Coverage: $($stats.OverallCoverage)%"
    Write-Host "  Files Analyzed: $($stats.TotalFiles)"
    Write-Host "  Total Lines: $($stats.TotalLines)"
    Write-Host "  Covered: $($stats.TotalCovered) | Uncovered: $($stats.TotalUncovered)"
    Write-Host ""

    if ($stats.OverallCoverage -lt $Threshold) {
        Write-Host "⚠ WARNING: Coverage is below the $Threshold% threshold!" -ForegroundColor Yellow
        Write-Host "  Gap: $($Threshold - $stats.OverallCoverage)%" -ForegroundColor Yellow
    }
    else {
        Write-Host "✓ Coverage meets the $Threshold% threshold" -ForegroundColor Green
    }

    Write-Host ""
    if ($Format -in @('Text', 'All')) {
        Write-Host "Reports generated:" -ForegroundColor Green
        Write-Host "  Text: $textReport"
    }
    if ($Format -in @('Html', 'All')) {
        Write-Host "  HTML: $htmlReport"
    }
    Write-Host ""
}
catch {
    Write-Host "Error generating coverage reports: $_" -ForegroundColor Red
    Write-Host "Stack trace: $($_.ScriptStackTrace)" -ForegroundColor Red
    exit 1
}
